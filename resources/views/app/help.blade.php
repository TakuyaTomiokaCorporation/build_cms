		<div class="article">
			<h2>TIPS</h2>
			<div class="section">
				<h3 id="20">オフラインで聴く</h3>
					<p>一度ダウンロードされてキャッシュ領域に保存された楽曲は、インターネットに繋がっていない状態でも再生可能です。オフラインモードを使用するとキャッシュに保存された楽曲のみ表示され素早く探すことができます。家ではWi-Fi、外出時はオフラインモードで楽しむときに便利です。</p>
					<p>曲名が濃い黒で表示されているものがキャッシュ済みの楽曲、灰色のものが未キャッシュ曲です。</p>
					<img src="{{ asset('images/app/ios-offline01.jpg') }}" alt="ios-offline01" class="sec-img img-responsive" />
					<p>左上の「設定」ボタンを押して設定画面を開きます、「キャッシュ済みの曲のみ表示」をオンにします。</p>
					<img src="{{ asset('images/app/ios-offlinemode01.jpg') }}" alt="ios-offline01" class="sec-img img-responsive" />
					<p>画面上部のバーが黒から灰色になります。 これが「オフラインモード」の目印です。</p>
					<img src="{{ asset('images/app/ios-offlinemode02.jpg') }}" alt="ios-offline02" class="sec-img img-responsive" />
					<p>インターネットに接続可能な環境下で未キャッシュ曲を再生する時には「キャッシュ済みの曲のみ表示」をオフにしてください。</p>
			</div>

			<div class="section">
				<h3 id="21">AirPlayで聴く</h3>
				<p>AVIOTアプリからAirPlay対応機器への出力が制御可能です。AirPlay対応機器と同じWi-Fiネットワークに接続すると自動的に、再生画面のボリューム調整スライダー右側にAirPlayアイコンが出現します。アイコンをタップすると出力先一覧が表示されます。</p>
				<img src="{{ asset('images/app/ios-airplay.jpg') }}" alt="ios-airplay" class="sec-img img-responsive" />
			</div>
		</div>

		<div class="article">
			<h2>よくある質問</h2>
			<div class="section">
				<h3 id="30">再生できない</h3>
				<ul>
					<li>インターネットに接続されていますか。接続を確認した上で再度お試しください。</li>
					<li>楽曲リストの一番上で引き下げてリストを最新の状態に更新してください。</li>
					<li>楽曲データが正しくダウンロードできていない可能性があります。設定画面の「iPhone上の曲ファイルを削除する」をタップしてキャッシュされたデータを削除して、もう一度楽曲データをダウンロードしなおしてください。</li>
				</ul>
			</div>

			<div class="section">
				<h3 id="31">iTunesの曲とプレイリストについて</h3>
				<p>iTunesライブラリの楽曲は、プレイリストに追加することはできません。</p>
			</div>

			<div class="section">
				<h3 id="32">「再生情報を送信」について</h3>
				<p>サービス向上のため、以下の情報が再生時に送信、収集されます。</p>
				<ul>
					<li>再生した曲のID</li>
				</ul>
				<p>送信を望まない場合、設定画面の「再生情報を送信」のチェックをオフにしてください。</p>
