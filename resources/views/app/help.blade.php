		<div class="article">
			<h2>ようこそ</h2>
			<div class="section">
				<h3 id="00">AVIOTアプリについて</h3>
				<p>「AVIOTアプリ」とは、読んで、聴いて、買える ミュージック・ストア「AVIOT」がお届けする音楽や情報を視聴できるアプリです。フリー・ダウンロード作品や新譜の視聴、楽曲にまつわる特集記事とニュースを読むなど、スマートフォンで手軽にAVIOTを楽しむことができます。</p>
				<p>さらに、OTOTOYアカウントを作成・ログインするとすべての機能が使用可能になります</p>
				<ul>
					<li>フリー・ダウンロード曲を「ライブラリに登録」することが可能になり、配信期間後も楽曲を楽しめます。</li>
					<li>OTOTOYで購入した楽曲を聴けます。</li>
					<li>「きになる」に新譜やランキングの楽曲を追加できます。あとで購入したい、というときに便利です。</li>
					<li>プレイリストを作成し、お気に入りの楽曲を集めることができます。</li>
					<li>同期したiTunesライブラリの楽曲が聴けます。</li>
				</ul>
			</div>

			<div class="section">
				<h3 id="01">ログインなしで使用する</h3>
				<p>OTOTOYのアカウントと一緒に使用すると便利なAVIOTアプリですが、新規登録やログインを後回しにしてひとまず使ってみる、ということも可能です。</p>
				<p>まずアプリを起動し、「初めてOTOTOYを使う」をタップします。</p>
				<img src="{{ asset('images/app/ios-login01.jpg') }}" alt="ios-login01" class="sec-img img-responsive">
				<p>次に「スキップ」をタップします。ユーザー登録のスキップについて確認がありますので、OKをタップしてください。</p>
				<img src="{{ asset('images/app/ios-login02.jpg') }}" alt="ios-login02" class="sec-img img-responsive">
				<p>これで、配信中の楽曲を視聴できます！</p>

				<p>アカウントの登録およびログインは、設定画面や「ライブラリ」タブ内設置されている「無料アカウントを作成」ボタンであとからでも可能です。</p>
			</div>

			<div class="section">
				<h3 id="02">QRコードでログインする</h3>
				<p>OTOTOYウェブサイト(<a href="https://ototoy.jp/">https://ototoy.jp/</a>)で購入した楽曲をスマホで簡単に聴くために、「ウェブとアプリの連携機能」を使ってQRコードでアプリにログインすることができます。</p>
				<p>OTOTOYのウェブページで楽曲をチェックアウトした後の購入完了画面、CollectionsのCampaignタブ(ゲストモードのみ)などに、「ウェブとアプリの連携」画面へのリンクがあります。このリンクから連携ページを表示します。
				</p>
				<img src="{{ asset('images/app/2app01.png') }}" alt="2app01" class="sec-img img-responsive">
				<p>連携ページに表示されるQRコードをログイン画面「QRコードでログインする」ボタンタップ後に読み取ることで連携が完了します。</p>
				<img src="{{ asset('images/app/2app02.png') }}" alt="2app02" class="sec-img img-responsive">
				<p>連携ページをスマホで表示した場合、QRコードではなく連携リンクが表示されます。アクセスするだけで端末内でアプリが開き連携が完了します。</p>
				<p>連携ページは<a href="https://OTOTOY.jp/auth/2app.php">こちら</a></p>
				<p>ウェブとアプリの連携機能について詳しくは<a href="https://OTOTOY.jp/feature/2app">こちら</a></p>
			</div>
		</div>

		<div class="article">
			<h2>おもな操作方法</h2>
			<div class="section">
			  <h3 id="10">各タブについて</h3>
			  <img src="{{ asset('images/app/ios-tab.png') }}" alt="ios-tab" class="sec-img img-responsive">
			  <ul>
			    <li><strong>今週の無料曲</strong> : 期間限定のフリーDLやフル試聴楽曲が無料で聞けます</li>
					<li><strong>試聴</strong> : 新譜や人気の楽曲一覧が表示され、試聴できます</li>
			    <li><strong>ニュース</strong> : OTOTOYで更新されているニュース一覧が表示されます。タップすると本文が表示されます</li>
			    <li><strong>ライブラリ</strong> : ライブラリに追加した曲の一覧が表示されます(要ログイン)</li>
			  </ul>
			</div>

			<div class="section">
				<h3 id="11">今週のフリーダウンロード曲を聴く</h3>
				<p>AVIOTアプリでは毎週、無料で再生できる楽曲を提供しています。<img src="{{ asset('images/app/ios-tab-pickup.png') }}" alt="ios-tab-pickup" class="li-img">「今週の無料曲」をタップすると、対象となる楽曲の一覧が表示されます。
				</p>
				<p>聴きたい楽曲をタップするとキャッシュ領域(※)に楽曲データのダウンロードが開始され、しばらくすると再生されます。</p>
			  <img src="{{ asset('images/app/ios-pickup02.jpg') }}" alt="ios-pickup02" class="sec-img img-responsive">
				<p class="hosoku">※キャッシュ領域とは、同じ楽曲を再生するたびにデータを取得しなおす必要がないように、一時的に楽曲データを蓄えておく領域のことをいいます。</p>

				<h4>ライブラリに追加する</h4>
				<p>ずっと聞いていたい楽曲と出会ったら、<img src="{{ asset('images/app/ios-addlibrary.png') }}" alt="ios-addlibrary" class="li-img">「ライブラリに追加」ボタンを押しましょう。するとライブラリに追加され、楽曲は期間が過ぎても再生可能です。ライブラリの楽曲は「アルバム」「曲」「アーティスト」タブからブラウズして再生できます。 <br />
					(既にライブラリに追加されている楽曲は代わりに「プレイリストに追加」ボタンが表示されます)</p>
				<img src="{{ asset('images/app/ios-library01.jpg') }}" alt="ios-library01" class="sec-img img-responsive">

				<h4>フル試聴曲について</h4>
				<p>今週の無料曲タブで表示されている楽曲のなかで、TRIALと表記されているものはフル試聴曲とよびます。TRIALアイコンの隣、または再生画面に表示されている日時まで再生可能ですが、ライブラリに楽曲の追加はできません。
				</p>
				<img src="{{ asset('images/app/ios-trial01.jpg') }}" alt="ios-trual01" class="sec-img img-responsive">
			</div>

			<div class="section">
				<h3 id="12">楽曲に関連する記事を読む</h3>
				<p><p><img src="{{ asset('images/app/ios-feature-icon.png') }}" alt="ios-feature-icon" class="li-img">記事アイコンタップすると楽曲のアーティストに関連する記事が表示されます。</p>
				<img src="{{ asset('images/app/ios-feature01.jpg') }}" alt="ios-feature01" class="sec-img img-responsive">
			</div>

			<div class="section">
				<h3 id="13">OTOTOYで購入した音源を聴く</h3>
				<p><img src="{{ asset('images/app/ios-tab-library.png') }}" alt="ios-tab-library" class="li-img">「ライブラリ」タブには過去OTOTOYで購入した楽曲と「ライブラリに追加」した楽曲がリストアップされています。<br />タップすると再生されます。</p>
				<img src="{{ asset('images/app/ios-library02.jpg') }}" alt="ios-library02" class="sec-img img-responsive">
			</div>

			<div class="section">
				<h3 id="14">プレイリストについて</h3>
				<p>プレイリストを使用すると、例えば好きなジャンルの楽曲だけを集めて好きな順番で再生することができます。</p>
				<h4>プレイリストの作成</h4>
				<p><img src="{{ asset('images/app/ios-tab-library.png') }}" alt="ios-tab-library" class="li-img">「ライブラリ」タブ内上部の「プレイリスト」をタップし、「追加」ボタンをタップすると、プレイリスト名を入力する画面が表示されます。</p>
				<img src="{{ asset('images/app/ios-playlist01.jpg') }}" alt="ios-playlist01" class="sec-img img-responsive">
				<p>名前を入力しOKをタップすると、曲を選択する画面が表示されます。タップすると文字が薄いグレーから黒色になり、これが追加された目印です。</p>
				<img src="{{ asset('images/app/ios-playlist02.jpg') }}" alt="ios-playlist02" class="sec-img img-responsive">
				<p>「完了」ボタンをタップするとプレイリストが作成されます。</p>

				<h4>プレイリストの編集</h4>
				<p>プレイリストを開き「編集」ボタンをタップすると、曲の追加、削除、再生順序の変更が可能です。</p>
				<img src="{{ asset('images/app/ios-playlist03.jpg') }}" alt="ios-playlist03" class="sec-img img-responsive">
				<ul>
					<li>追加する:「追加」ボタンをタップします</li>
					<li>削除する: <img src="{{ asset('images/app/del_7.jpg') }}" alt="del" class="li-img">をタップします。</li>
					<li>並び替える : <img src="{{ asset('images/app/sort_7.jpg') }}" alt="sort" class="li-img">を押さえながら上下に動かします</li>
				</ul>
				<img src="{{ asset('images/app/ios-playlist04.jpg') }}" alt="ios-playlist04" class="sec-img img-responsive">
				<p>編集を終えたら、「完了」ボタンをタップします。</p>
				<h4>プレイリストの名前変更と削除</h4>
				<ul>
					<li>名前を変更する : プレイリストをタップして開き「名前変更」ボタンを押して新しい名前を入力しOKをタップします。</li>
					<li>プレイリストを削除する : プレイリスト一覧で「編集」ボタンをタップし、削除したいプレイリストの<img src="{{ asset('images/app/del_7.jpg') }}" alt="del" class="li-img">をタップします。</li>
				</ul>
				<h4>再生画面から追加する</h4>
				<p>「プレイリスト追加」ボタンをタップすと現在作成されているプレイリストの一覧が表示されます。タップしたプレイリストに再生中の楽曲が追加されます。</p>
				<img src="{{ asset('images/app/ios-playlist05.jpg') }}" alt="ios-playlist05" class="sec-img img-responsive">
			</div>

			<div class="section">
				<h3 id="15">リピートとシャッフル再生</h3>
				<h4>リピート再生</h4>
				<img src="{{ asset('images/app/repeat_mode.jpg') }}" alt="repeat_mode" class="sec-img img-responsive">
				<p>上図のように再生画面のリピートボタンを押すたびにリピートのモードが切り替わります。</p>
				<h4>シャッフル再生</h4>
				<p>再生画面からと曲の選択画面からシャッフル再生できます。</p>
				<ul>
					<li>再生画面の<img src="{{ asset('images/app/shuffle_off.jpg') }}" alt="shuffle_off" class="li-img">をタップすると再生順がシャッフルされます。<img src="{{ asset('images/app/shuffle_on.jpg') }}" alt="shuffle_on" class="li-img">は既にシャッフル再生中です。タップすると通常の再生順に戻ります。</li>
					<li>プレイリスト、「曲」タブの上部にある「シャッフル」をタップするとシャッフル再生がはじまります。</li>
					<li>アルバムの楽曲一覧画面にある<img src="{{ asset('images/app/album_shuffle_button.jpg') }}" alt="album_shuffle_button" class="li-img">をタップするとアルバムのシャッフル再生ができます。</li>
				</ul>
			</div>

			<div class="section">
				<h3 id="16">再生位置の変更、音量の調整</h3>
				<p>楽曲再生画面のジャケット真下のバーで再生位置の変更ができます。</p>
				<p>画面最下部のバーで音量の調整ができます。スライダーを左右に動かして調整します。</p>
				<img src="{{ asset('images/app/ios-position.jpg') }}" alt="ios-position" class="sec-img img-responsive">
			</div>

			<div class="section">
				<h3 id="17">特典ファイルの表示</h3>
				<p>購入特典のあるアルバムには、プレゼントの箱のような「特典アイコン <img src="{{ asset('images/app/special_icon.jpg') }}" alt="special_icon" class="li-img"> 」が表示されるので、それをタップします。</p>
				<img src="{{ asset('images/app/ios-bonus01.jpg') }}" alt="ios-bonus01" class="sec-img img-responsive">
				<p>すると、特典ファイルの一覧が表示されます。</p>
				<img src="{{ asset('images/app/ios-bonus03.jpg') }}" alt="ios-bonus03" class="sec-img img-responsive">
				<p>JPEGなどの画像ファイル、PDFファイルが閲覧できます。</p>
				<img src="{{ asset('images/app/ios-bonus04.jpg') }}" alt="ios-bonus04" class="sec-img img-responsive">
			</div>

