			<h2>TIPS</h2>
			<div class="section">
				<h3 id="20">オフラインで聴く</h3>
					<p>一度ダウンロードされてキャッシュ領域に保存された楽曲は、インターネットに繋がっていない状態でも再生可能です。オフラインモードを使用するとキャッシュに保存された楽曲のみ表示され素早く探すことができます。家ではWi-Fi、外出時はオフラインモードで楽しむときに便利です。</p>
					<p>曲名が濃い黒で表示されているものがキャッシュ済みの楽曲、灰色のものが未キャッシュ曲です。</p>
					<img src="{{ asset('images/app/ios-offline01.jpg') }}" alt="ios-offline01" class="sec-img img-responsive" />
					<p>左上の「設定」ボタンを押して設定画面を開きます、「キャッシュ済みの曲のみ表示」をオンにします。</p>
